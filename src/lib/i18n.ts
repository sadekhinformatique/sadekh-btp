export const translations: Record<Lang, TranslationDict> = { fr, wo };

export function t(key: string, lang: Lang): string {
  return translations[lang]?.[key] || translations.fr[key] || key;
}

export function formatPrice(price: number, lang: Lang): string {
  const formatted = new Intl.NumberFormat(lang === 'fr' ? 'fr-FR' : 'fr-SN').format(price);
  return `${formatted} ${t('common.fcf', lang)}`;
}